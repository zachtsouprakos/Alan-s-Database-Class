-- Stored Procedure & Trigger  new_employeeAssign() --
create or replace function new_employeeAssign() returns trigger as $new_employeeAssign$
declare 
    open_spotID int := (select parkingspot.spotid  
						from employeepark e full outer join parkingspot on e.spotid = parkingspot.spotid
						where (e.spotid <= 2471 and e.spotid >= 2121) or (e.spotid <= 600 and e.spotid >= 301) or (e.spotid <= 1900 and e.spotid >= 1601) or (e.spotid <= 2010 and e.spotid >= 2001) or (e.spotid <= 2020 and e.spotid >= 2011) or (e.spotid <= 2120 and e.spotid >= 2021) or (e.spotid <= 2918 and e.spotid >= 2771) or (e.spotid <= 3020 and e.spotid >= 2921) or (e.spotid <= 3220 and e.spotid >= 3021) or (e.spotid <= 3970 and e.spotid >= 3823) isnull
						limit 1);

begin 
    insert into employeepark(employeeid, spotid)
		values(new.employeeID ,open_spotID);
    return new;
End;
$new_employeeAssign$ language plpgsql;

